
# if is the first condition
# else is the last condition
# elif is between them
# else doesn't need any condition
# if a condition is true, then the interpretor will execute the statement inside that condition

a = 70
b = 60

if a > b:
    print(a, "is greater than", b)
elif a < b:
    print(a, "is less than", b)  
elif a == b:
    print(a, "is equal to", b)
else:
    print("The system failed to execute this order")    


print("------------------------------------------------------------") 

